    public static $currentLang = null;
    public static function LoadLanguage() {
        if (Language::$currentLang === null) {
            if (@isset($_COOKIE["lang"])) {
                self::$currentLang = $_COOKIE["lang"];
            } else {
                self::$currentLang = "en";
            }
        }
    }
    public static function SetLanguage(string $code) {
        $opts =  [
            'expires' => time() + (86400 * 30 * 365),
            'path' => '/',
            'secure' => true,
            'samesite' => 'None'
        ];
        setcookie('lang', $code, $opts);
        self::$currentLang = $code;
    }
    public static function Translate(string $short) : string {
        self::LoadLanguage();
        if (!isset(self::$strings[$short])) {
            return $short;
        }
        if (!isset(self::$strings[$short][self::$currentLang])) {
            return array_values(self::$strings[$short])[0];
        }
        return self::$strings[$short][self::$currentLang];
    }
